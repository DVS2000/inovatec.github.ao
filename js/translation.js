let currentLanguage = "en";

// Atualiza os textos no HTML
function updateTexts() {
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    const translation = translations[currentLanguage][key];
    if (translation) {
      element.textContent = translation;
    } else {
      console.warn(`Chave de tradução não encontrada: ${key}`);
    }
  });
}

// Muda o idioma ao selecionar no <select>
function changeLanguage() {
  const languageSelect = document.getElementById("languageSwitcher");
  const selectedLanguage = languageSelect.value;

  if (translations[selectedLanguage]) {
    currentLanguage = selectedLanguage;
    updateTexts();
    const selectedOption = languageSelect.options[languageSelect.selectedIndex];
    const imagePath = selectedOption.getAttribute("data-image");
    languageSelect.style.backgroundImage = `url(${imagePath})`;
  } else {
    console.error(`Idioma não suportado: ${selectedLanguage}`);
  }
}

// Inicializa o i18n com o idioma padrão
document.addEventListener("DOMContentLoaded", () => {
  updateTexts();
});
